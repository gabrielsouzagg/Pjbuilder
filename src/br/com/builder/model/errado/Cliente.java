
package br.com.builder.model.errado;


public class Cliente {
    private String nome;
     private String endereço;
      private String cidade;
       private String cpf;
        private String rg;
         private String email;
          private String telefone;

    public Cliente(String nome, String endereço, String cidade, String cpf, String rg, String email, String telefone) {
        this.nome = nome;
        this.endereço = endereço;
        this.cidade = cidade;
        this.cpf = cpf;
        this.rg = rg;
        this.email = email;
        this.telefone = telefone;
    }
          
          
          
       
}
